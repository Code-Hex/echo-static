func performRequest(r http.Handler, method, path string) *httptest.ResponseRecorder {
	req, _ := http.NewRequest(method, path, nil)
	w := httptest.NewRecorder()
	r.ServeHTTP(w, req)
	return w
}

func TestBinaryPath(t *testing.T) {

	filename := "test.txt"

	router := echo.New()
	router.Use(ServeRoot("/static", NewAssets("assets")))

	w := performRequest(router, "GET", "/")
	assert.Equal(t, http.StatusNotFound, w.Code)

	w = performRequest(router, "GET", "/static")
	assert.Equal(t, http.StatusOK, w.Code)
	assert.Equal(t, "<pre>\n<a href=\"test.txt\">test.txt</a>\n</pre>", strings.TrimSpace(w.Body.String()))

	w = performRequest(router, "GET", "/static/"+filename)
	assert.Equal(t, http.StatusOK, w.Code)
	assert.Equal(t, "Echo Web Framework", strings.TrimSpace(w.Body.String()))
}

func NewAssets(root string) *assetfs.AssetFS {
	return &assetfs.AssetFS{
		Asset:     Asset,
		AssetDir:  AssetDir,
		AssetInfo: AssetInfo,
		Prefix:    root,
	}
}

